<h1 class="mb-4">家計簿アプリについて</h1>

<div class="card mb-4">
    <div class="card-body">
        <h2 class="card-title">目的</h2>
        <p class="card-text">
            この家計簿アプリは、日々の支出を簡単に記録し、予算管理を効率的に行うためのツールです。
            自身の財務状況を把握し、健全な家計管理をサポートすることを目的としています。
        </p>
    </div>
</div>

<div class="card mb-4">
    <div class="card-body">
        <h2 class="card-title">主な機能</h2>
        <ul class="list-group list-group-flush">
            <li class="list-group-item">予算設定：食事とその他の支出に対して日々の予算を設定</li>
            <li class="list-group-item">支出記録：毎日の支出を種類別（朝食、昼食、夕食、その他）に記録</li>
            <li class="list-group-item">日次概要：設定した予算と実際の支出を比較し、差額を視覚化</li>
            <li class="list-group-item">週次分析：週ごとの支出傾向をグラフと表で表示</li>
        </ul>
    </div>
</div>

<div class="card mb-4">
    <div class="card-body">
        <h2 class="card-title">期待する課題解決</h2>
        <ul class="list-group list-group-flush">
            <li class="list-group-item">日々の支出を意識し、無駄な出費を抑える習慣づけ</li>
            <li class="list-group-item">予算と実際の支出の差を即座に確認し、家計の健全性を維持</li>
            <li class="list-group-item">週単位での支出傾向を把握し、長期的な家計改善のヒントを獲得</li>
            <li class="list-group-item">食費とその他の支出を分けて管理することで、より詳細な家計分析が可能</li>
        </ul>
    </div>
</div>

<div class="card">
    <div class="card-body">
        <h2 class="card-title">使い方</h2>
        <ol class="list-group list-group-numbered">
            <li class="list-group-item">まず「予算設定」ページで、1食あたりの予算と1日のその他支出予算を設定します。</li>
            <li class="list-group-item">日々の支出を「支出記録」ページで記録します。日付、種類、金額、メモを入力してください。</li>
            <li class="list-group-item">「ホーム」ページで、今日の予算と支出の概要を確認できます。</li>
            <li class="list-group-item">「分析」ページでは、週ごとの支出傾向をグラフと表で確認できます。</li>
        </ol>
    </div>
</div>